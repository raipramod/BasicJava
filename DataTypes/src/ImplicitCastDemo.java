
public class ImplicitCastDemo {

	public static void main(String[] args) {
		
//		Implicit Type Casting is to convert from lower data type to higher data type
		
		byte b = 120;
		int i = b;
		
		System.out.println(i);
		
		long l = i;
		
		char ch = 'A';
		
//		The value of 'A' Char is 65 
		
		int x = ch;
		System.out.println(x);
		
	}

}
